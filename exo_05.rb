#  #{}  sert a integrer des nombre , un calcule, une comparaison... a l'interieur des chaine de caractere string

puts "On va compter le nombre d'heures de travail à THP"#affiche le texte sur une ligne
puts "Travail : #{10 * 5 * 11}"# affiche le texte, execute le calcul et affiche le résultat sur la meme ligne
puts "En minutes ça fait : #{10 * 5 * 11 * 60}"# affiche le texte, execute le calcul et affiche le resultat sur la meme ligne

puts "Et en secondes ?"# affiche le texte sur une ligne

puts 10 * 5 * 11 * 60 * 60# execute le calcul et affiche le resultat sur une ligne

puts "Est-ce que c'est vrai que 3 + 2 < 5 - 7 ?"# affiche "..le texte.." sur une ligne

puts 3 + 2 < 5 - 7# demande d afficher le resultat de la comparaison, il affichera ci c'est true ou false

puts "Ça fait combien 3 + 2 ? #{3 + 2}"# affiche le texte, execute le calcul et affiche le résultat sur la meme ligne
puts "Ça fait combien 5 - 7 ? #{5 - 7}"# affiche le texte, execute le calcul et affiche le résultat sur la meme ligne

puts "Ok, c'est faux alors !"# affiche le texte string 

puts "C'est drôle ça, faisons-en plus :"# affiche le texte string

puts "Est-ce que 5 est plus grand que -2 ? #{5 > -2}"# affiche le texte string + le resultat de la comparaison
puts "Est-ce que 5 est supérieur ou égal à -2 ? #{5 >= -2}"# affiche le texte string + le resultat de la comparaison
puts "Est-ce que 5 est inférieur ou égal à -2 ? #{5 <= -2}"# affiche le texte string + le resultat de la comparaison